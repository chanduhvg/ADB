DROP TABLE PART;
DROP TABLE CUSTOMER;
DROP TABLE SLSREP;

CREATE TABLE SLSREP(
repNum	NUMBER(5),
repName	VARCHAR2(30),
repAddress	VARCHAR2(30),
totComm	NUMBER,
commRate NUMBER,
CONSTRAINT slsrep_repNum_pk PRIMARY KEY( repNum) );

CREATE TABLE CUSTOMER(
custNumber  NUMBER(5),
custName VARCHAR2(30),
address VARCHAR2(30),
currBal NUMBER,
credLimit NUMBER, 
repNum NUMBER(5),
CONSTRAINT customer_custNumber_pk PRIMARY KEY (custNumber),
CONSTRAINT customer_repNum_fk FOREIGN KEY (repNum) 
  REFERENCES SLSREP(repNUM) );

CREATE TABLE PART(
partNum	VARCHAR2(20),
partDesc VARCHAR2(20),
unitsOnHand NUMBER(5),
itemClass VARCHAR2(20),
warehouseNum NUMBER(5),
unitPrice NUMBER,
CONSTRAINT part_partNum_pk PRIMARY KEY (partNum));


CREATE TABLE CUSTORDER(
orderNum NUMBER(7),
orderDate DATE,
custNumber NUMBER(5),
CONSTRAINT custorder_orderNum_pk PRIMARY KEY (orderNum),
CONSTRAINT custorder_custNum_fk FOREIGN KEY (custNumber) 
  REFERENCES CUSTOMER(custNumber) );

CREATE TABLE ORDERLINE (
orderNum NUMBER(7),
partNum	VARCHAR2(20),
numOrdered NUMBER(7),
quotedPrice NUMBER,
CONSTRAINT orderline_ordernum_partnum_pk 
  PRIMARY KEY (orderNum, partNum),
CONSTRAINT orderline_ordernum_fk FOREIGN KEY (orderNum) 
  REFERENCES CUSTORDER(orderNum),
CONSTRAINT orderline_partnum_fk FOREIGN KEY (partNum) 
  REFERENCES PART(partNum) );

